// defaults.cpp -- cout 默认格式化输出演示
#include <iostream>

int main()
{
    using std::cout;
    cout << "12345678901234567890\n";  // 输出一个数字字符串作为参考标尺

    char ch = 'K';                      // 声明并初始化一个字符变量
    int t = 273;                        // 声明并初始化一个整数变量
    cout << ch << ":\n";                // 显示字符
    cout << t << ":\n";                 // 显示整数
    cout << -t << ":\n";                // 显示负数

    double f1 = 1.200;                  // 声明并初始化一个双精度浮点数
    cout << f1 << ":\n";                // 显示浮点数
    cout << (f1 + 1.0 / 9.0) << ":\n";  // 显示浮点数运算结果

    double f2 = 1.67E2;                 // 声明并初始化一个科学计数法表示的浮点数
    cout << f2 << ":\n";                // 显示科学计数法数值
    f2 += 1.0 / 9.0;                    // 加上 1/9
    cout << f2 << ":\n";                // 显示结果
    cout << (f2 * 1.0e4) << ":\n";      // 显示大数值计算结果

    double f3 = 2.3e-4;                 // 声明并初始化一个小数值
    cout << f3 << ":\n";                // 显示小数值
    cout << f3 / 10 << ":\n";           // 显示更小的数值

    return 0;
}
